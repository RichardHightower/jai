package com.cloudurable.jai.model.audio;

/**
 * Enumeration of audio response formats.
 */
public enum AudioResponseFormat {

    /**
     * JSON format for the audio response.
     */
    JSON,

    /**
     * Text format for the audio response.
     */
    TEXT,

    /**
     * SRT format for the audio response.
     */
    SRT,

    /**
     * VTT format for the audio response.
     */
    VTT,

    /**
     * Verbose JSON format for the audio response.
     */
    VERBOSE_JSON,

    /**
     * Other format for the audio response.
     */
    OTHER
}
