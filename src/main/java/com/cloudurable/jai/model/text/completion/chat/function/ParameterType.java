package com.cloudurable.jai.model.text.completion.chat.function;

/**
 * Represents the types of parameters in a chat system.
 */
public enum ParameterType {
    /**
     * Represents an object parameter type.
     */
    OBJECT,

    /**
     * Represents an array parameter type.
     */
    ARRAY,

    /**
     * Represents a number parameter type.
     */
    NUMBER,

    /**
     * Represents a string parameter type.
     */
    STRING
}
